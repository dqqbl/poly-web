const FAIR_PROB_MAP = new Map<string, number>();
for (const [diff, rb, prob] of FAIR_PROB_RAW) {
  FAIR_PROB_MAP.set(`${diff},${rb}`, prob);
}

export function getRemBin(rem: number): string | null {
  // rem ≥ 240 都归到 240-300 档（处理 rem = 300 或窗口刚切换时的瞬时值）
  if (rem >= 240) return "240-300";
  if (rem >= 180) return "180-240";
  if (rem >= 120) return "120-180";
  if (rem >= 60) return "60-120";
  if (rem >= 30) return "30-60";
  if (rem >= 0) return "0-30";
  return null;  // rem < 0（窗口已结束）
}

/** 根据 diff 和 rem 查合理概率（%）；查不到返回 null */
export function getFairProb(diff: number, rem: number): number | null {
  const rb = getRemBin(rem);
  if (!rb) return null;
  // diff 超出映射表范围时，夹到最近边界（±95 → 映射到 ±140/±95 之间）
  const rawDb = Math.round(diff / 5) * 5;
  const db = Math.max(-140, Math.min(95, rawDb));
  return FAIR_PROB_MAP.get(`${db},${rb}`) ?? null;
}
